function clamp01(t: number): number {
  return t < 0 ? 0 : t > 1 ? 1 : t;
}

function interpolate(scale: readonly string[], t: number): string {
  const last = scale.length - 1;
  const x = clamp01(t) * last;
  const i = Math.min(Math.floor(x), last);
  const lo = scale[i] as string;
  if (i === last) return lo;
  const hi = scale[i + 1] as string;
  const frac = x - i;
  const a = hexToRgb(lo);
  const b = hexToRgb(hi);
  const r = Math.round(a[0] + (b[0] - a[0]) * frac);
  const g = Math.round(a[1] + (b[1] - a[1]) * frac);
  const bl = Math.round(a[2] + (b[2] - a[2]) * frac);
  return rgbToHex(r, g, bl);
}

function hexToRgb(hex: string): [number, number, number] {
  const v = parseInt(hex.slice(1), 16);
  return [(v >> 16) & 0xff, (v >> 8) & 0xff, v & 0xff];
}

function rgbToHex(r: number, g: number, b: number): string {
  return '#' + ((r << 16) | (g << 8) | b).toString(16).padStart(6, '0');
}

/**
 * Return a hex color from the Viridis scale at position t ∈ [0, 1].
 * Linearly interpolates between the two nearest stops — smoother than
 * nearest-neighbor lookup for continuous encodings (density, time).
 */
export function viridis(t: number): string {
  return interpolate(VIRIDIS_256, t);
}

/**
 * Return a hex color from the Cividis scale at position t ∈ [0, 1].
 * Prefer Cividis over Viridis when the distinction between adjacent stops
 * must remain legible under deuteranopia or protanopia.
 */
export function cividis(t: number): string {
  return interpolate(CIVIDIS_256, t);
}
